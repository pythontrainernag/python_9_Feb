"""This module/script is used to perform all arthematic operations"""

def addi(first_var, sec_var):
    """ This function is used perform the addition of two numbers"""
    return first_var+sec_var

def subs(first_var, sec_var):
    """ This function is used perform the substraction of two numbers"""
    return first_var-sec_var

def mult(first_var, sec_var):
    """ This function is used perform the multiplication of two numbers"""
    return first_var*sec_var
